enum my_keycodes {
    MK_KANA = SAFE_RANGE,
};

void toggle_kana(keyrecord_t *record) {
    if (!record->event.pressed) {
        return;
    }

    if (detected_host_os() == OS_WINDOWS) {
        SEND_STRING(SS_LALT("`"));
    } else {
        tap_code(KC_CAPS);
    }
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case MK_KANA:
            toggle_kana(record);
            break;
    }

    return true;
}

